import UIKit

struct Color {
    var darkGray: UIColor {
        return UIColor(red: 45/255, green: 49/255, blue: 66/255, alpha: 1)
    }
    
    var softRed: UIColor {
        return UIColor(red: 239/255, green: 131/255, blue: 84/255, alpha: 1)
    }
    
    var light: UIColor {
        return .white
    }
}
